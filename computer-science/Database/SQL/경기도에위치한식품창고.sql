SELECT WAREHOUSE_ID, 
       WAREHOUSE_NAME, 
       ADDRESS, 
       COALESCE(FREEZER_YN, 'N') AS FREEZER_YN
FROM FOOD_WAREHOUSE
WHERE ADDRESS LIKE '경기도%'
ORDER BY WAREHOUSE_ID;



-- SQL에서 LIKE 연산자는 문자열 패턴 매칭을 위해 사용되며, 주로 WHERE 절과 함께 사용됩니다. LIKE 연산자에서 사용되는 %는 와일드카드 문자의 일종으로, 0개 이상의 임의의 문자 시퀀스를 나타냅니다. 이 와일드카드는 특정 패턴과 일치하는 문자열을 찾는 데 유용합니다.

-- %의 사용 예제와 설명

-- 	1.	%의 단독 사용
-- 	•	예시: LIKE '%경기%'
-- 	•	설명: 문자열 내의 어느 위치에든 “경기”라는 문자열이 포함된 모든 값을 찾습니다. %가 앞뒤로 사용되어, “경기”라는 단어의 앞뒤에 어떤 문자열이 있더라도 상관없이 매칭됩니다.
-- 	2.	앞에 % 없이 사용
-- 	•	예시: LIKE '경기%'
-- 	•	설명: “경기”로 시작하는 모든 문자열을 찾습니다. 이 경우, “경기”로 시작하고 그 뒤에 어떤 문자들이 있든 상관없이 일치합니다.
-- 	3.	뒤에 % 없이 사용
-- 	•	예시: LIKE '%경기'
-- 	•	설명: “경기”로 끝나는 모든 문자열을 찾습니다. 문자열의 끝이 “경기”로 끝나는지 확인합니다.

-- 구체적 예시

-- 	•	주소가 ‘경기도 안산시’인 경우
-- 	•	LIKE '경기도%'는 일치합니다. ‘경기도’로 시작하기 때문입니다.
-- 	•	주소가 ‘서울시 강남구’인 경우
-- 	•	LIKE '경기도%'는 일치하지 않습니다. ‘경기도’로 시작하지 않기 때문입니다.

-- 따라서, LIKE '경기도%'는 주소가 “경기도”로 시작하는 모든 행을 선택하는 조건을 의미하며, ADDRESS 컬럼 값이 “경기도”로 시작하는지 여부를 판단하게 됩니다. %는 그 이후의 임의의 문자 시퀀스를 의미하므로, “경기도” 다음에 어떤 문자열이 오든지 상관없습니다.